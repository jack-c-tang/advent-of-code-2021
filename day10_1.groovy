closing = [
    ')': [open: '(', score: 3],
    ']': [open: '[', score: 57],
    '}': [open: '{', score: 1197],
    '>': [open: '<', score: 25137],
]

score = 0
closingKeys = closing.keySet()
codes.eachWithIndex { code, ci ->
    def list = []
    def failed = false
    code.split('').eachWithIndex { c, i ->
        if (failed) {
            return
        } else if (!(c in closingKeys) || list.size() == 0) {
            list << c
        } else {
            if (list[list.size()-1] != closing[c].open) {
                println("ci: ${ci}, i: ${i}")
                score += closing[c].score
                failed = true
                return
            } else {
                list.remove(list.size()-1)
            }
        }
    }
}
